/**
 * localgovjp オープンデータから市町村一覧（座標付き）を生成する。
 * https://github.com/code4fukui/localgovjp
 */
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const SOURCE_URL = 'https://code4fukui.github.io/localgovjp/localgovjp.json'
const OUT_PATH = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'lib', 'municipalitiesData.ts')

const PREFECTURE_ORDER = [
  '北海道', '青森県', '岩手県', '宮城県', '秋田県', '山形県', '福島県',
  '茨城県', '栃木県', '群馬県', '埼玉県', '千葉県', '東京都', '神奈川県',
  '新潟県', '富山県', '石川県', '福井県', '山梨県', '長野県', '岐阜県',
  '静岡県', '愛知県', '三重県', '滋賀県', '京都府', '大阪府', '兵庫県',
  '奈良県', '和歌山県', '鳥取県', '島根県', '岡山県', '広島県', '山口県',
  '徳島県', '香川県', '愛媛県', '高知県', '福岡県', '佐賀県', '長崎県',
  '熊本県', '大分県', '宮崎県', '鹿児島県', '沖縄県',
]

const res = await fetch(SOURCE_URL)
if (!res.ok) throw new Error(`fetch failed: ${res.status}`)
const rows = await res.json()

const byPref = Object.fromEntries(PREFECTURE_ORDER.map((name) => [name, []]))

for (const row of rows) {
  const pref = row.pref
  if (!byPref[pref]) continue
  byPref[pref].push({
    name: row.city,
    lat: Number.parseFloat(row.lat),
    lng: Number.parseFloat(row.lng),
  })
}

for (const pref of PREFECTURE_ORDER) {
  byPref[pref].sort((a, b) => a.name.localeCompare(b.name, 'ja'))
}

const body = `// Auto-generated by scripts/generate-municipalities.mjs — do not edit by hand
import type { PrefectureName } from './prefectures'

export type Municipality = {
  name: string
  lat: number
  lng: number
}

export const MUNICIPALITIES_BY_PREFECTURE: Record<PrefectureName, Municipality[]> = ${JSON.stringify(byPref, null, 2)} as Record<PrefectureName, Municipality[]>
`

writeFileSync(OUT_PATH, body, 'utf8')
const total = Object.values(byPref).reduce((n, list) => n + list.length, 0)
console.log(`Wrote ${total} municipalities to ${OUT_PATH}`)
